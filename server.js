function spacedDigits(str, group = 4) {
  return String(str || "").replace(/\D/g, "").match(new RegExp(`.{1,${group}}`, "g"))?.join(" ") || "";
}
function last4(str) {
  const digits = String(str || "").replace(/\D/g, "");
  return digits.slice(-4);
}
